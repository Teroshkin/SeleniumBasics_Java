public class JavaConstanta_Lesson15 {
    public static void main (String []args){
        final double Pi = 3.1415926536;
        // Константа Pi с типом данных для чисел с плавающей точкой
        double length1 = 2*Pi*10;
        double length2 = 2*Pi*25;
        System.out.println("При радиусе 10 см " + length1 + ", а при радиусе 25 cм "+length2);
    }
}
